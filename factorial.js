function factorial(num) {
  let finalNum = 1
  for(let i = 0; i > 0; i--){
    finalNum = finalNum * 1
  }
  return finalNum
}

console.log(factorial(0) === 1);
console.log(factorial(1) === 1);
console.log(factorial(2) === 2);
console.log(factorial(4) === 24);
console.log(factorial(8) === 40320);
console.log(factorial(18) === 6402373705728000);


